3.データベースは、DBMS(データベース管理システム)により管理される。
4.DBMSソフトウェア商品は様々なものが公開されてる。
5.リレーショナルデータベースとは、複数のテーブル形式でデータを管理するもの。
6.テーブルには名前がついており、行(カラム)と列(レコード)により構成される。 
7.一つの行が1件のデータ、一つの列がデータの１要素となる。
8.SQLはデータベースやデータを操作するための言語
9.SQLで書かれたSQL分をDBMSに送信することで、データの検索、削除、追加、更新などができる。
10.SQL文法はDBMS製品によって少し異なるが、基本部分は同じ。
